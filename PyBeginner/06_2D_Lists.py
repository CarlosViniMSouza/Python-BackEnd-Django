# Well, here we doing 2 lists in 1 list. It's simple. Example:

my_matrix = [
    [0, 3, 9],
    [2, 4, 6],
    [10, 12, 14]
]

print(my_matrix) # See the compressed list.

print(my_matrix[0][0]) # Accessing the index of matrix.

# We can use For Loops to run 'my_matrix'
for cont in my_matrix:
    print(cont)